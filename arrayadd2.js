// Create an array named 'studentNames' with some student names
const studentNames = ["Ram","Shyam", "Leela", "Roshan"];

// Add a new student name to the beginning of the array by using unshift
studentNames.unshift("Mokshi"); // Adds 'Mochi' to the beginning

// Remove the last student name from the array by using pop
studentNames.pop(); // Removes 'Emily' (the last name)

// Alphabetize the student names within the array by using sort
studentNames.sort(); // Sorts the names in alphabetical order

// Output the manipulated array
console.log(studentNames);